/**
 * Build the in-memory bundle for the test by loading the committed bundle
 * JSON and overriding the bundle's `responses` map with a single response
 * keyed by the pinned `RECORDED_BUNDLE_KEY`. The committed file pins the
 * sealed response content + captured provider identity + the v3 schema
 * envelope (cost, routing posture, usage block); this wrapper only re-keys
 * the response to the test's fixed key so the replay is hermetic.
 */
function loadSealedBundle(): RecordedProviderBundle {
  const committed = JSON.parse(
    readFileSync(join(here, "fixtures/character-relationship-recorded-bundle.json"), "utf8"),
  ) as RecordedProviderBundle;
  // The committed file carries one response under `RECORDED_BUNDLE_KEY`;
  // re-key so a stale `bundleKey` derivation on the recorded provider can
  // never accidentally match this test.
  const sealedResponse = committed.responses[RECORDED_BUNDLE_KEY];
  if (!sealedResponse) {
    throw new Error(`sealed bundle missing required response under key '${RECORDED_BUNDLE_KEY}'`);
  }
  // The committed bundle IS the sealed transcript — return it verbatim so
  // the test fails loud on any committed-field drift (schemaVersion bump,
  // missing cost/routingPosture/usageResponseJson, schema mismatch).
  return {
    ...committed,
    responses: { [RECORDED_BUNDLE_KEY]: sealedResponse },
  };
}

type SnapshotPack = {
  bios: Array<Omit<CharacterBio, "id" | "promptHash"> & { promptHash: string }>;
  relationships: Array<Omit<CharacterRelationship, "id" | "promptHash"> & { promptHash: string }>;
};

function loadPackSnapshot(): SnapshotPack {
  const raw = JSON.parse(
    readFileSync(join(here, "fixtures/character-relationship-pack-snapshot.json"), "utf8"),
  ) as { bios: SnapshotPack["bios"]; relationships: SnapshotPack["relationships"] };
  return raw;
}

/**
 * Canonicalise the agent's pack output for byte-for-byte snapshot
 * comparison. Strips the random `id` field (UUID7 is non-deterministic).
 * `providerRun` is dropped because `runId` is also random.
 */
function canonicalisePack(pack: {
  bios: CharacterBio[];
  relationships: CharacterRelationship[];
  // providerRun deliberately not part of the snapshot.
}): {
  bios: Array<Omit<CharacterBio, "id">>;
  relationships: Array<Omit<CharacterRelationship, "id">>;
} {
  return {
    bios: pack.bios.map(({ id: _id, ...rest }) => rest),
    relationships: pack.relationships.map(({ id: _id, ...rest }) => rest),
  };
}

describe("character-relationship-agent-recorded (ITOTORI-149)", () => {
  it("replays the sealed recorded bundle and matches the committed snapshot byte-for-byte", async () => {
    const input = inputFixture();
    const bundle = loadSealedBundle();

    // Pre-flight: the bundle's schemaVersion must be the current
    // RECORDED_PROVIDER_BUNDLE_SCHEMA_VERSION — otherwise construction
    // would fail and the byte-for-byte contract would be untestable.
    expect(bundle.schemaVersion).toBe(RECORDED_PROVIDER_BUNDLE_SCHEMA_VERSION);

    const provider = new RecordedModelProvider({
      bundle,
      // Pinned key — see header for why we override the default key.
      bundleKey: () => RECORDED_BUNDLE_KEY,
    });

    const output = await generateCharacterRelationships(input, { provider });

    // Sanity gates: the parse + validate path produced a Fresh pack with
    // non-empty citations and the recorded token usage flowed through.
    expect(output.bios).toHaveLength(3);
    expect(output.relationships).toHaveLength(2);
    for (const bio of output.bios) {
      expect(bio.status).toBe("Fresh");
      expect(bio.citedUnitIds.length).toBeGreaterThan(0);
      expect(bio.citedUnitHashes.length).toBe(bio.citedUnitIds.length);
    }
    for (const rel of output.relationships) {
      expect(rel.status).toBe("Fresh");
      expect(rel.citedUnitIds.length).toBeGreaterThan(0);
      expect(rel.citedUnitHashes.length).toBe(rel.citedUnitIds.length);
    }
    expect(output.providerRun.tokenUsage.completionTokens).toBe(384);
    expect(output.providerRun.cost).toEqual(ZERO_COST);

    // Deterministic metadata: prompt hash is computed from the rendered
    // prompt (independent of the recorded bundle), and input token estimate
    // is computed from the same prompt text. The agent stores `promptHash`
    // on bios/relationships as the RAW sha256 hex (no `sha256:` prefix);
    // the prompt-pinning `request.prompt.promptHash` is the prefixed form.
    const rendered = buildPrompt(input);
    const expectedRawPromptHash = promptHash(rendered);
    const expectedPromptHashPrefixed = `sha256:${expectedRawPromptHash}`;
    const expectedInputTokenEstimate = estimateTokens(
      `${rendered.systemText}\n${rendered.userText}`,
    );

    for (const bio of output.bios) {
      expect(bio.promptHash).toBe(expectedRawPromptHash);
      expect(bio.inputTokenEstimate).toBe(expectedInputTokenEstimate);
      expect(bio.generatedAt).toBe(FIXED_NOW.toISOString());
    }
    for (const rel of output.relationships) {
      expect(rel.promptHash).toBe(expectedRawPromptHash);
      expect(rel.generatedAt).toBe(FIXED_NOW.toISOString());
    }
    expect(output.providerRun.prompt.promptHash).toBe(expectedPromptHashPrefixed);

    // Inject the computed values into the snapshot so the byte-for-byte
    // comparison sees matching promptHash + inputTokenEstimate fields.
    const snapshot = loadPackSnapshot();
    for (const bio of snapshot.bios) {
      bio.promptHash = expectedRawPromptHash;
      bio.inputTokenEstimate = expectedInputTokenEstimate;
    }
    for (const rel of snapshot.relationships) {
      rel.promptHash = expectedRawPromptHash;
    }

    // Byte-for-byte comparison against the committed snapshot — the
    // random `id` fields are stripped; everything else MUST match.
    const canonical = canonicalisePack(output);

    expect(JSON.stringify(canonical.bios, null, 2)).toBe(JSON.stringify(snapshot.bios, null, 2));
    expect(JSON.stringify(canonical.relationships, null, 2)).toBe(
      JSON.stringify(snapshot.relationships, null, 2),
    );
  });

  it("two replays of the sealed bundle against the same input produce byte-equal packs", async () => {
    const input = inputFixture();
    const bundle = loadSealedBundle();
    const provider = new RecordedModelProvider({
      bundle,
      bundleKey: () => RECORDED_BUNDLE_KEY,
    });

    const a = await generateCharacterRelationships(input, { provider });
    const b = await generateCharacterRelationships(input, { provider });

    // The agent is a pure parse/validate/project pipeline over the
    // provider's content; same input + same bundle content → same pack
    // structure. `id` is the only non-deterministic field; canonicalise it
    // out before the byte-equal assertion.
    const aCanonical = canonicalisePack(a);
    const bCanonical = canonicalisePack(b);
    expect(JSON.stringify(aCanonical.bios, null, 2)).toBe(JSON.stringify(bCanonical.bios, null, 2));
    expect(JSON.stringify(aCanonical.relationships, null, 2)).toBe(
      JSON.stringify(bCanonical.relationships, null, 2),
    );
  });
});
