// ITOTORI-149 — Character-relationship agent recorded-provider replay test
// (end-to-end gap #1 deferred by ITOTORI-014).
//
// `generateCharacterRelationships` is driven by a SEALED provider transcript
// committed at
// `apps/itotori/test/fixtures/character-relationship-recorded-bundle.json`.
// The replay proves the agent exercises the same parse → validate →
// project-to-record path as a real provider call WITHOUT touching the
// network, then asserts the bios+relationships pack matches the committed
// snapshot at
// `apps/itotori/test/fixtures/character-relationship-pack-snapshot.json`
// BYTE-FOR-BYTE (deterministic).
//
// Determinism surface — what is fixed and why:
//   • `schemaVersion` of the recorded bundle is locked to
//     RECORDED_PROVIDER_BUNDLE_SCHEMA_VERSION (v3); pre-v3 bundles fail at
//     `RecordedModelProvider` construction with a typed
//     RecordedBundleSchemaMismatchError.
//   • Each `RecordedProviderResponse` carries the required `tokenUsage`,
//     `cost`, `routingPosture`, and `usageResponseJson` per ITOTORI-228 /
//     230 / 232 / genaudit2-01 — no char/4 estimate laundering.
//   • The fixed `bundleKey` override (`"char-rel-recorded-bundle-001-key"`)
//     means the bundle's lookup key is independent of the input's prompt
//     hash (which would otherwise drift on any prompt-template change). The
//     test would still find the response after a template bump because the
//     key is pinned here, not derived — this is the canonical pattern for a
//     "sealed" replay.
//   • `now` (the `fixedNow` test seam on CharacterRelationshipInput) pins
//     `generatedAt`, and `promptHash` is computed from the deterministic
//     rendered prompt.
//
// What is NOT in the snapshot and why:
//   • `id` on bios + relationships is generated by `createUuid7` and is
//     therefore random (and not stable across runs). The test strips `id`
//     before comparing to the snapshot; this is the ONLY non-deterministic
//     field on the pack. (ProviderRunRecord is excluded from the pack
//     snapshot entirely — its `runId` is also random via
//     `createProviderRunId`.)

