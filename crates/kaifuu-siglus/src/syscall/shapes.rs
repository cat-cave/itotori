//! Oracle-grounded names for the global system-call namespace.
//!
//! `siglus_rs`'s `siglus_ss_decompiler::constants::BUILTIN_ELEMENTS`
//! `ElementDef { parent: "global", code, name, form, kind }` table is the
//! ABI source for this map. Each slot records the oracle name and result form.
//! `siglus_scene_vm::vm::CD_COMMAND` establishes the argument grammar shared
//! by those slots: the encoded argument list supplies its exact, possibly
//! variable-length forms, and the named-id tail labels its final operands.
//!
//! The entries deliberately cover the complete contiguous global namespace
//! described by that table. Individual call sites retain their own forms,
//! values, and named-argument ids because optional and overloaded calls do
//! not have a single fixed arity.

/// Return the oracle-defined `(name, result_form)` tuple for a global slot.
///
/// A call's complete argument shape remains the decoded `CD_COMMAND`
/// argument list; it cannot be replaced with a guessed fixed arity.
pub(super) fn system_function_shape(function_id: i32) -> Option<(&'static str, &'static str)> {
    let index = usize::try_from(function_id).ok()?;
    Some((
        *SYSTEM_FUNCTION_NAMES.get(index)?,
        *SYSTEM_FUNCTION_RESULT_FORMS.get(index)?,
    ))
}

/// Return the oracle-defined name of a global system-function slot.
pub(super) fn system_function_name(function_id: i32) -> Option<&'static str> {
    system_function_shape(function_id).map(|(name, _)| name)
}

