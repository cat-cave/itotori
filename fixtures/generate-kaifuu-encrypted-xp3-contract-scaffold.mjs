// --- Manifest ----------------------------------------------------------------

function writeManifest() {
  const manifest = {
    $schema: "./manifest.schema.json",
    schemaVersion: "0.1.0",
    fixture: {
      id: fixtureId,
      title: "Kaifuu Synthetic Encrypted-XP3 Contract Scaffolding",
      kind: "synthetic",
      summary:
        "Synthetic, public-redistributable encrypted-XP3 fixture that keeps the detect -> key resolution -> extract -> patch -> verify -> delta-apply contract surface continuously tested in public CI. Not a retail encrypted-XP3 readiness claim.",
      sourceLocale: "ja-JP",
      targetLocales: ["en-US"],
      publicRedistribution: "allowed",
      license: {
        spdx: "CC0-1.0",
        evidence: `Generated synthetic bytes and JSON authored in-repository by fixtures/generate-${fixtureId}.mjs.`,
      },
      provenance: {
        author: "Kaifuu fixture authors",
        creationMethod:
          "Deterministic generator writes a synthetic XP3-CRYPT envelope, a real plain inner XP3, a public fixture-only key manifest, and a harness descriptor. No retail bytes.",
        rawAssetPolicy: "contains-no-copyrighted-game-assets",
      },
    },
    files: files
      .map((file) => {
        const path = resolve(repoRoot, file.path);
        const content = readFileBytes(path);
        return {
          ...file,
          sha256: createHash("sha256").update(content).digest("hex"),
          bytes: statSync(path).size,
          redistributable: true,
        };
      })
      .sort((left, right) => left.path.localeCompare(right.path)),
    aggregateStats: {
      files: files.length,
      textUnits: 0,
      sourceTextUtf16CodeUnits: 0,
      protectedSpans: 0,
      sourceLocales: ["ja-JP"],
      targetLocales: ["en-US"],
      engineKinds: ["kiri-kiri-xp3-synthetic-encrypted-contract-scaffold"],
      notes:
        "All bytes are generated fixture-only data. The encrypted envelope is an XP3-CRYPT marker; the decrypted inner archive is a real plain XP3. No retail game bytes, keys, or decrypted text.",
    },
    benchmarkUse: {
      allowedInPublicCi: true,
      allowedInPublicReports: true,
      reportingLabel: `public:${fixtureId}`,
    },
  };
  writeFileBytes(manifestPath, Buffer.from(stableJson(manifest), "utf8"));
}

// --- Writers -----------------------------------------------------------------

function writeJson(relativePath, value, role, mediaType = "application/json") {
  writeText(relativePath, stableJson(value), role, mediaType);
}

function writeText(relativePath, text, role, mediaType) {
  writeBytes(relativePath, Buffer.from(text, "utf8"), role, mediaType);
}

function writeBytes(relativePath, content, role, mediaType) {
  const path = resolve(fixtureRoot, relativePath);
  writeFileBytes(path, content);
  files.push({
    path: `fixtures/public/${fixtureId}/${relativePath}`,
    role,
    mediaType,
  });
}

function writeFileBytes(path, content) {
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, content);
}

function readFileBytes(path) {
  return Buffer.from(readFileSync(path));
}

// --- XP3 byte format (mirrors the proven builder in the matrix generator) ----

function plainXp3Fixture(entries) {
  const chunks = [];
  chunks.push(Buffer.from([0x58, 0x50, 0x33, 0x0d, 0x0a, 0x20, 0x0a, 0x1a, 0x8b, 0x67, 0x01]));
  chunks.push(leU64(0n));

  const segmentOffsets = [];
  let offset = chunks.reduce((sum, chunk) => sum + chunk.length, 0);
  for (const entry of entries) {
    segmentOffsets.push(BigInt(offset));
    chunks.push(entry.payload);
    offset += entry.payload.length;
  }

  const indexOffset = BigInt(offset);
  const indexChunks = [];
  for (const [index, entry] of entries.entries()) {
    const pathBytes = utf16le(entry.path);
    const info = Buffer.concat([
      leU32(0),
      leU64(BigInt(entry.payload.length)),
      leU64(BigInt(entry.payload.length)),
      leU16(pathBytes.length / 2),
      pathBytes,
    ]);
    const segment = Buffer.concat([
      leU32(entry.compressed ? 1 : 0),
      leU64(segmentOffsets[index]),
      leU64(BigInt(entry.payload.length)),
      leU64(BigInt(entry.payload.length)),
    ]);
    indexChunks.push(
      xp3Chunk(
        "File",
        Buffer.concat([
          xp3Chunk("info", info),
          xp3Chunk("segm", segment),
          xp3Chunk("adlr", leU32(entry.adler32)),
        ]),
      ),
    );
  }

  const index = Buffer.concat(indexChunks);
  chunks.push(Buffer.from([0]));
  chunks.push(leU64(BigInt(index.length)));
  chunks.push(index);

  const archive = Buffer.concat(chunks);
  leU64(indexOffset).copy(archive, 11);
  return archive;
}

function xp3Chunk(name, content) {
  return Buffer.concat([Buffer.from(name, "ascii"), leU64(BigInt(content.length)), content]);
}

function utf16le(text) {
  const output = Buffer.alloc(text.length * 2);
  for (let index = 0; index < text.length; index += 1) {
    output.writeUInt16LE(text.charCodeAt(index), index * 2);
  }
  return output;
}

function leU16(value) {
  const buffer = Buffer.alloc(2);
  buffer.writeUInt16LE(value);
  return buffer;
}

function leU32(value) {
  const buffer = Buffer.alloc(4);
  buffer.writeUInt32LE(value >>> 0);
  return buffer;
}

function leU64(value) {
  const buffer = Buffer.alloc(8);
  buffer.writeBigUInt64LE(value);
  return buffer;
}

function adler32(buffer) {
  let a = 1;
  let b = 0;
  const MOD = 65521;
  for (const byte of buffer) {
    a = (a + byte) % MOD;
    b = (b + a) % MOD;
  }
  return ((b << 16) | a) >>> 0;
}

function bytes(text) {
  return Buffer.from(text, "utf8");
}

function stableJson(value) {
  // Match the repo formatter (Biome via `vp check`), which collapses a
  // single-element string array onto one line. Emitting that form directly
  // keeps `node generate` output identical to the committed, formatter-clean
  // bytes — so the manifest hashes stay consistent with what CI validates.
  const pretty = JSON.stringify(value, null, 2);
  const collapsed = pretty.replace(/\[\n\s+("(?:[^"\\]|\\.)*")\n\s*\]/g, "[$1]");
  return `${collapsed}\n`;
}
