#!/usr/bin/env node
// synthetic-fixture-coverage-catalogue-per-engine (P2).
//
// Emits a COVERAGE MANIFEST: every UNIQUE component the REAL-VALIDATED
// corpora + real-bytes tests currently exercise, enumerated per engine
// family, each entry tagged with the exact source (catalogue constant /
// enum / real-bytes assertion) that exercises it. The manifest is the
// basis for later AUTHORING feature-complete synthetic archives that
// exercise each component exactly once (fast) instead of re-parsing whole
// real games (~30 min).
//
// PROJECT LAW: evidence-first, DERIVED-not-invented. Every component is
// EXTRACTED from a real source-of-truth file (the same catalogue constants
// / enums / assertions the real-bytes tests key on) — never hand-authored.
// The `--check` mode re-derives the manifest from those sources and fails
// (exit 1) if the committed manifest omits ANY source-derived component, so
// the manifest CANNOT silently drift below real coverage: adding a new
// real-bytes-exercised component (e.g. a REAL_CATALOG tuple) without
// re-generating the manifest fails the check.
//
// Usage:
//   node scripts/synthetic-coverage-manifest.mjs           # write manifest
//   node scripts/synthetic-coverage-manifest.mjs --check    # fail on drift
//
// The check is a byte-for-byte + structured (missing/extra component) diff
// between the committed manifest and the freshly re-derived one.

